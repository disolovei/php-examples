		'Ї' => 'Yi',
		'Й' => 'Y',
		'К' => 'K',
		'Л' => 'L',
		'М' => 'M',
		'О' => 'O',
		'П' => 'P',
		'Р' => 'R',
		'С' => 'S',
		'Т' => 'T',
		'У' => 'U',
		'Ф' => 'F',
		'Х' => 'H',
		'Ц' => 'Ts',
		'Ч' => 'Ch',
		'Ш' => 'Sh',
		'Щ' => 'Shch',
		'Ь' => '',
		'Ю' => 'Yu',
		'Я' => 'Ya',
		'Ъ' => '',
		'ъ' => '',
		'Ы' => 'Y',
		'ы' => 'y',
		'Э' => 'e',
		'э' => 'e',
		'Ё' => 'ye',
		'ё' => 'ye',
	];

	return str_replace( array_keys( $letters ), array_values( $letters ), $cyrillic_str );
}
